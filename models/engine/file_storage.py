#!/usr/bin/python3
"""
This module updates the FileStorage class to manage serialization and
deserialization
of instances for all new classes: Place, State, City, Amenity, and Review.
"""

import os
import json
import datetime

class FileStorage:

    """
    This class handles serialization and deserialization of instances to
    and from a JSON file.
    """
    __file_path = "file.json"
    __objects = {}

    def all(self):
        """Provides the dictionary named "__objects."""
        return FileStorage.__objects

    def new(self, obj):
        """
        Assigns the object with the key "<object class name>.id" in
        the "__objects" set.
        """
        key = "{}.{}".format(type(obj).__name__, obj.id)
        FileStorage.__objects[key] = obj

    def save(self):
        """
        Serializes the "__objects" to a JSON file located at
        "__file_path.
        """
        with open(FileStorage.__file_path, "w", encoding="utf-8") as f:
            d = {k: v.to_dict() for k, v in FileStorage.__objects.items()}
            json.dump(d, f)

    def classes(self):
        """
        Provides a dictionary containing valid classes and their
        corresponding references.
        """
        from models.base_model import BaseModel
        from models.user import User
        from models.state import State
        from models.city import City
        from models.amenity import Amenity
        from models.place import Place
        from models.review import Review

        classes = {"BaseModel": BaseModel,
                   "User": User,
                   "State": State,
                   "City": City,
                   "Amenity": Amenity,
                   "Place": Place,
                   "Review": Review}
        return classes

    def reload(self):
        """
        Deserializes the JSON file to __objects.
        Only reloads if the JSON file exists.
        """
        if not os.path.isfile(FileStorage.__file_path):
            return
        with open(FileStorage.__file_path, "r", encoding="utf-8") as f:
            obj_dict = json.load(f)
            obj_dict = {k: self.classes()[v["__class__"]](**v)
                        for k, v in obj_dict.items()}
            # TODO: Does this operation involve overwriting or inserting?
            FileStorage.__objects = obj_dict

    def attributes(self):
        """
        Provides the valid attributes and their corresponding types for
        the given class name.
        """
        attributes = {
            "BaseModel":
                     {"id": str,
                      "created_at": datetime.datetime,
                      "updated_at": datetime.datetime},
            "User":
                     {"email": str,
                      "password": str,
                      "first_name": str,
                      "last_name": str},
            "State":
                     {"name": str},
            "City":
                     {"state_id": str,
                      "name": str},
            "Amenity":
                     {"name": str},
            "Place":
                     {"city_id": str,
                      "user_id": str,
                      "name": str,
                      "description": str,
                      "number_rooms": int,
                      "number_bathrooms": int,
                      "max_guest": int,
                      "price_by_night": int,
                      "latitude": float,
                      "longitude": float,
                      "amenity_ids": list},
            "Review":
            {"place_id": str,
                         "user_id": str,
                         "text": str}
        }
        return attributes